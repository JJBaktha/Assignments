print ( " This program will divide 2 numbers and show the answer and the remainder " )
number_1 = float ( input ( " Please enter the first number : " ))
number_2 = float ( input ( " Please enter the second number : " ))

total_answer = number_1 // number_2
total_remainder = number_1 % number_2

print ( " This second number goes into the first number {0} times and the remainder is {1} " .format ( total_answer, total_remainder))
